package Estructuras;

import javax.swing.table.DefaultTableModel;

/**
 *
 * @author Rolo
 */
public class TablaLugares extends DefaultTableModel {

         public TablaLugares(Object[][] data, Object[] columnNames) {
             super(data, columnNames);
         }

      @Override
      public Class<?> getColumnClass(int columnIndex) {
                    Class<?> clazz = Object.class;
      Object aux = getValueAt(0, columnIndex);
       if (aux != null) {
        clazz = aux.getClass();
       }

       return clazz;
      }

}
